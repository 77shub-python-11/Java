package com.opp.polymorphism;

public class Overloading {
	/**
	 * overloading is also called as static binding / early binding 
	 * in this method name is same and list of parameter must be different
	 * methods should exist in same class 
	 *  
	 */
	
	public int add(int x, int y ) {
		
		
		
		return x+y;
	}
	public int add(int x,int y , int z ) {
		return x+y+z;
		
	}
	

}
